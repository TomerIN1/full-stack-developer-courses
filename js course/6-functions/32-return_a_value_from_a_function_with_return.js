// you can return a value from a function with this return statement. 

function minusSeven(num){ // we are passing a number into it - the num. 
    return num - 7; // then it is going to return whatever is after teh return keyword. 
}

console.log(minusSeven(10)); // here we are console.log the function and the result is 3. why? the 10 assign to the num. 10-7 is 3.


