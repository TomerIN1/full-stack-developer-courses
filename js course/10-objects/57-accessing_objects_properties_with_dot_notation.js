// there are two main ways to access a property on an object. 
// the first called dot notation. 

// example
var ourDog = { 
    "name": "Bisli", 
    "legs": 4, 
    "tails": 1, 
    "friends": ['everything!']
}; 


// use dot notation to access the value of a given property. 
var dogName = ourDog.name
var dogLegs = ourDog.legs

console.log("the dog name is: " + dogName);
console.log("he has " + dogLegs + " legs"); 

